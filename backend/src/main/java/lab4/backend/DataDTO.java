package lab4.backend;

public class DataDTO {
    private double x;
    private double y;
    private double r;

    // Конструктор по умолчанию
    public DataDTO() {}

    // Геттеры и сеттеры
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }
}
